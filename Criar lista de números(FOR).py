print("Primeira lista (Padrão):")
# Sempre usar os ':' quando for atribuir ação aos programas de execução ou condição.
for x in range(10):
    print(x)

print("Segunda lista (inicio delimitado):")
# valor inicial do range é 0, mas pode ser modificado
for x in range(5,10):
    print(x)

print("terceira lista (decrescente):")
# paara colocar em ordem descrecente colocamos o maximo e o minino e olocamos apos o '-1'
for x in range(10,0,-1):
    print(x)

